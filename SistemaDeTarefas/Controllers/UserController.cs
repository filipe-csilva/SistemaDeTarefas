using Microsoft.AspNetCore.Mvc;
using ApiDeTarefas.Data.Dto;
using ApiDeTarefas.Data.Dtos;
using ApiDeTarefas.Models;
using ApiDeTarefas.Repository.Interfaces;

namespace ApiDeTarefas.Controllers
{
    [Route("api/[controller]")]
    [ApiController]
    public class UserController : ControllerBase
    {
        private readonly IUserRepository _userRepository;

        public UserController(IUserRepository userRepository)
        {
            this._userRepository = userRepository;
        }

        [HttpGet]
        public async Task<ActionResult<ICollection<ReadUserDto>>> SearchAllUsers()
        {
            ICollection<UserMd> usuarios = await _userRepository.GetAll();
            return Ok(usuarios);
        }

        [HttpGet("{id}")]
        public async Task<ActionResult<ReadUserDto>> SearchUserById(int id)
        {
            UserMd? usuario = await _userRepository.SearchById(id);

            if (usuario == null) return NotFound();

            ReadUserDto userReturn = new ReadUserDto()
            {
                Id = usuario.Id,
                Name = usuario.Name,
                Email = usuario.Email
            };

            return userReturn;
        }

        [HttpPost]
        public async Task<ReadUserDto> AddUser(CreateUserDto userDto)
        {
            UserMd usuario = new UserMd()
            {
                Name = userDto.Name,
                Email = userDto.Email
            };

            await _userRepository.Add(usuario);

            ReadUserDto userReturn = new ReadUserDto()
            {
                Id = usuario.Id,
                Name = usuario.Name,
                Email = usuario.Email
            };

            return userReturn;
        }

        [HttpPut("id")]
        public async Task<ReadUserDto> UpdateUserById(UpdateUserDto user, int id)
        {
            UserMd usuario = new UserMd()
            {
                Id = id,
                Name = user.Name,
                Email = user.Email
            };

            await _userRepository.Update(usuario, id);

            ReadUserDto userReturn = new ReadUserDto()
            {
                Id = usuario.Id,
                Name = usuario.Name,
                Email = usuario.Email
            };

            return userReturn;
        }

        [HttpDelete("id")]
        public async Task<UserMd> DropUser(int id)
        {
            UserMd usuario = await _userRepository.Delete(id);
            return usuario;
        }
    }
}

